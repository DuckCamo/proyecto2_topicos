    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int cmb_subject = this.cmb_subjects.getSelectedIndex() + 1;

        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select id from inscriptions where subject = '" + cmb_subject + "' and user = '" + this.id + "'");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "Estudiante ya inscrito en la asignatura.");
                cn.close();
            } else {

                cn.close();

                try {

                    Connection cn2 = Conexion.conectar();
                    PreparedStatement pst2 = cn2.prepareStatement(
                            "insert into inscriptions values (?,?,?)");
                    pst2.setInt(1, 0);
                    pst2.setInt(2, id);
                    pst2.setInt(3, cmb_subject);
                    
                    pst2.executeUpdate();
                    cn2.close();

                    JOptionPane.showMessageDialog(null, "Registro Exitoso.");
                    this.dispose();

                } catch (SQLException e) {
                    System.err.println("Error en Registrar isncripci√≥n. " + e);
                    JOptionPane.showMessageDialog(null, "Error! al registrar, contacte al administrador");
                }

            }

        } catch (SQLException e) {
            System.err.println("Error en validar que ya esta incrito " + e);
            JOptionPane.showMessageDialog(null, "Error! validar que ya esta inscrito, contacate al administrador.");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

