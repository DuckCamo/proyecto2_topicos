    private void cmb_addSubjects() {

        int i = 0;

        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select name from subjects");
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {

                cmb_subjects.addItem(rs.getString(i + 1));
            }
        } catch (SQLException e) {
            System.err.println("Error! al llenar el combobox de asignaturas." + e);
        }
    }

