    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int gender_cmb, validacion = 0;
        String name, lastnamef, lastnamem, age, phone, username, pass, pass_encritada, gender_string = "";

        name = txt_name.getText().trim();
        lastnamef = txt_lastnamef.getText().trim();
        lastnamem = txt_lastnamem.getText().trim();
        age = txt_age.getText().trim();
        phone = txt_phone.getText().trim();
        username = txt_user.getText().trim();
        pass = txt_pass.getText().trim();
        gender_cmb = cmb_gender.getSelectedIndex() + 1;
        
        if (name.equals("")) {
            txt_name.setBackground(Color.red);
            validacion++;
        }
        if (lastnamef.equals("")) {
            txt_lastnamef.setBackground(Color.red);
            validacion++;
        }
        if (lastnamem.equals("")) {
            txt_lastnamem.setBackground(Color.red);
            validacion++;
        }
        if (age.equals("")) {
            txt_age.setBackground(Color.red);
            validacion++;
        }
        if (phone.equals("")) {
            txt_phone.setBackground(Color.red);
            validacion++;
        }
        if (username.equals("")) {
            txt_user.setBackground(Color.red);
            validacion++;
        }
        if (pass.equals("")) {
            txt_pass.setBackground(Color.red);
            validacion++;
        }
        
        switch (gender_cmb) {
            case 1:
                gender_string = "Femenino";
                break;
            case 2:
                gender_string = "Masculino";
                break;
            case 3:
                gender_string = "Otro";
                break;
            default:
                break;
        }
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
            "select user from users where user = '" + username + "'");
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                txt_user.setBackground(Color.red);
                JOptionPane.showMessageDialog(null, "Nombre de usuario no disponible.");
                cn.close();
            } else {
                
                cn.close();
                
                if (validacion == 0) {
                    
                    cripto cripto = new cripto();
                    pass_encritada = cripto.Encriptar(pass);
                    
                    try {
                        
                        Connection cn2 = Conexion.conectar();
                        PreparedStatement pst2 = cn2.prepareStatement(
                            "insert into users values (?,?,?,?,?,?,?,?,?,?)");
                        pst2.setInt(1, 0);
                        pst2.setString(2, name);
                        pst2.setString(3, lastnamef);
                        pst2.setString(4, lastnamem);
                        pst2.setString(5, gender_string);
                        pst2.setString(6, age);
                        pst2.setString(7, username);
                        pst2.setString(8, pass_encritada);
                        if (title.equals("Administrador")){
                            pst2.setString(9, "Administrador");
                        } else {
                            pst2.setString(9, "Estudiante");
                        }
                        
                        pst2.setString(10, phone);
                        
                        pst2.executeUpdate();
                        cn2.close();
                        
                        Clean();
                        
                        txt_name.setBackground(Color.GREEN);
                        txt_lastnamef.setBackground(Color.GREEN);
                        txt_lastnamem.setBackground(Color.GREEN);
                        txt_age.setBackground(Color.GREEN);
                        txt_phone.setBackground(Color.GREEN);
                        txt_user.setBackground(Color.GREEN);
                        txt_pass.setBackground(Color.GREEN);
                        cmb_gender.setBackground(Color.GREEN);
                        
                        JOptionPane.showMessageDialog(null, "Registro Exitoso.");
                        this.dispose();
                        
                    } catch (SQLException e) {
                        System.err.println("Error en Registrar usuario. " + e);
                        JOptionPane.showMessageDialog(null, "Error! al registrar, contacte al administrador");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Debes llenar todos los campos.");
                }
                
            }
            
        } catch (SQLException e) {
            System.err.println("Error en validar nombre de usuario " + e);
            JOptionPane.showMessageDialog(null, "Error! al comparar usuario, contacate al administrador.");                    
        }
    }//GEN-LAST:event_jButton1ActionPerformed

