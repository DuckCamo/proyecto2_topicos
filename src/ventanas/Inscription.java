    private void Tabla() {

        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select inscriptions.id, subjects.name from inscriptions, subjects where inscriptions.user = '" +Login.id 
                            + "' and inscriptions.subject = subjects.id order by inscriptions.id");
            ResultSet rs = pst.executeQuery();

            jTable_admins = new JTable(model);
            jScrollPane1.setViewportView(jTable_admins);

            model.addColumn("");
            model.addColumn("Asignatura");

            while (rs.next()) {
                Object[] fila = new Object[2];

                for (int i = 0; i < 2; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);
            }
            cn.close();

        } catch (SQLException e) {
            System.err.println("Error! en el llenado de la tabla users." + e);
        }
    }

